#!/usr/bin/env python3

"""
    a type-annotated function add that takes a float a and a float b
    as arguments and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    Adds two floating-point numbers and returns the result.

    Parameters:
        a (float): The first number to be added.
        b (float): The second number to be added.

    Returns:
        float: The sum of 'a' and 'b'.
    """
    return a + b
